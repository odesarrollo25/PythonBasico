''' 
Para dividir dos numeros en Python, veremos division entera y división regular vamos a crear 4 variables
num1, num2, result y result1, posteriormente vamos a imprimir
'''
num1 = 5
num2 = 3
result = num1/num2
result1 = num1%num2

print(f'El resultado de la división regular ', num1, "/", num2, "=", result)

